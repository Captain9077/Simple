                <h1>The spicy and delectable Hakka noodles</h1>
                <p class="fs-6 pt-3">The all-time favourite Indian Chinese dish of Hakka noodles originated from the Chinese population settled in Kolkata. Hakka means ‘guest people’. Over the years, Hakka noodles have become very popular and are served right from the street vendors selling Chinese fast foods to the most high-class upscale restaurant. The Hakka noodles, a top-selling dish at Food On Way, is a delicacy enjoyed by our patrons. The subtle way in which we combine the vegetables and sauces through stir frying to retain the crunchiness of the veggies and the flavour of the sauces that blend with the bland boiled Hakka noodles imparts a distinct taste to it.
                    <br>
                    <br>
                    Here are some tips from our Chef that you can use if you are a first-timer trying to cook Hakka noodles at home. These little tricks make our Hakka noodles the top-selling Chinese food on the menu.
                    <br>
                    <br>
                    Chop the vegetables thin so that they blend well with the noodles while stir-frying.
                    <br>
                    Add oil to the boiled noodles to prevent them from clumping together.
                    <br>
                    It is very essential not to overcook the noodles. This can be ensured by running cold tap water once you drain the noodles from boiling water.
                    <br>
                    A large wok to stir fry and a high flame impart the much desired smoky flavour to our Hakka noodles making them yummilicious!
